// Helper function to get translations
export const getTranslation = (languageCode, path) => {
  // Default to English if the language code is not available
  const language = translations[languageCode] || translations.en;
  
  // Split the path by dots and traverse the translations object
  const pathArray = path.split('.');
  let result = language;
  
  for (const key of pathArray) {
    if (result && result[key]) {
      result = result[key];
    } else {
      // If the translation is not found, return the English version
      let englishResult = translations.en;
      for (const key of pathArray) {
        if (englishResult && englishResult[key]) {
          englishResult = englishResult[key];
        } else {
          return path; // Last fallback: return the path itself
        }
      }
      return englishResult;
    }
  }
  
  return result;
};

// Function to get a translated text
export const t = (path, languageCode = 'en') => {
  return getTranslation(languageCode, path);
};
